public class FeedSantaControl {
   
    public double IsSantaFull(double calories) {
        
        
     if (calories >= needed) {
       return 0;
     }
    
    else {
        return -1;
     }
     
     double needed = 2200;
     
     return calories;
     
    }
   
}
